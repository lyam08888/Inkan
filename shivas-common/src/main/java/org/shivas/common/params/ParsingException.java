package org.shivas.common.params;

public class ParsingException extends Exception {

	private static final long serialVersionUID = -1496717873756480148L;

	public ParsingException() {
	}

	public ParsingException(String arg0) {
		super(arg0);
	}

	public ParsingException(Throwable arg0) {
		super(arg0);
	}

	public ParsingException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
